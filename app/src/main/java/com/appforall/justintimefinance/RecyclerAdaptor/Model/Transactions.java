package com.appforall.justintimefinance.RecyclerAdaptor.Model;

public class Transactions {

    private String transInfoTextView, dateTextView, balanceTextView;
    private int imageView;

    public String getTransInfoTextView() {
        return transInfoTextView;
    }

    public void setTransInfoTextView(String transInfoTextView) {
        this.transInfoTextView = transInfoTextView;
    }

    public String getDateTextView() {
        return dateTextView;
    }

    public void setDateTextView(String dateTextView) {
        this.dateTextView = dateTextView;
    }

    public String getBalanceTextView() {
        return balanceTextView;
    }

    public void setBalanceTextView(String balanceTextView) {
        this.balanceTextView = balanceTextView;
    }

    public int getImageView() {
        return imageView;
    }

    public void setImageView(int imageView) {
        this.imageView = imageView;
    }
}
